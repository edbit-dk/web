<h2>Syddansk Erhvervsskole. I Odense-Vejle-Grindsted</h2>
<p>
- mere end 5.200 elever kan ikke tage fejl…
<br>
Syddansk Erhvervsskole Odense-Vejle er blandt Danmarks største tekniske skoler med ca. 5.277 årselever og 872 medarbejdere.
<br>
Uddannelser
Syddansk Erhvervsskole udbyder 45 forskellige erhvervsuddannelser – fordelt på 10 indgange - og tekniske gymnasier i både Odense og Vejle. Hertil kommer en lang række kurser og tilbud om efteruddannelse.
<br>
Syddansk Erhvervsskole er en fusion
Syddansk Erhvervsskole, der er en fusion mellem Odense Tekniske Skole og Vejle Tekniske Skole, har rødder, der går tilbage til 1844. 
<br>
I dag dækker Syddansk Erhvervsskole en del af den syddanske region, og skolen har 14 adresser i henholdsvis Jylland og på Fyn. 
<br>
Selvejende institution
Syddansk Erhvervsskole drives som en selvejende institution, som ledes af en bestyrelse.
